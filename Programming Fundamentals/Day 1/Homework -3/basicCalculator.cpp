#include <iostream>
using namespace std;
int main()
{
	int num1 = 6;
	int num2 = 7;
	int ans;

	ans = num1 + num2;
	cout << "\nThe 1st Answer is " << ans;

	ans = num1 - num2;
	cout << "\nThe 2nd Answer is " << ans;

	ans = num1 * num2;
	cout << "\nThe 3rd Answer is " << ans;

	ans = num1 / num2;
	cout << "\nThe 4th Answer is " << ans;

	ans = num1 % num2;
	cout << "\nThe 5th Answer is " << ans;
	return 0;
}
